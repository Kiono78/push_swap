#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*output;
	size_t	i;

	output = (char *)malloc(sizeof(*output) * (ft_strlen(s) + 1));
	if (output == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		output[i] = s[i];
		i++;
	}
	output[i] = '\0';
	return (output);
}
