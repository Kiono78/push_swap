#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*output;
	unsigned int	length;
	unsigned int	i;

	if (!s)
		return (NULL);
	length = ft_strlen(s);
	output = (char *)malloc(sizeof(char) * (length + 1));
	if (output == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		output[i] = f(i, s[i]);
		i++;
	}
	output[i] = '\0';
	return (output);
}
